            <h1>PLAZA SATÉLITE</h1>
            <p>
            En este municipio se encuentra uno de los centros comerciales pioneros en el país establecidos a principios de la década de los setenta del siglo XX: Plaza Satélite. Después de una construcción de aproximadamente tres años, en octubre de 1971 se inauguró este centro comercial, que en sus inicios contaba con cuatro firmas comerciales: Sears Roebuck, El Puerto de Liverpool, Sanborns y París Londres, de los cuales solo los tres primeros continúan operando. Además de estas tiendas, actualmente existen decenas de locales comerciales.</p>
